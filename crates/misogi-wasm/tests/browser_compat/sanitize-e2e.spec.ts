test.describe('Large File Handling', () => {

  /**
   * Test: Verify the application handles larger files without crashing.
   *
   * Browser memory limits can cause issues with large file processing:
   * - Chrome: ~512MB-2GB per tab (varies by system RAM)
   * - Firefox: ~800MB-2GB per tab
   * - Safari: ~1GB-4GB per tab
   *
   * This test generates a ~10MB file to verify:
   * 1. File can be uploaded without browser freeze
   * 2. Large file warning is displayed (if >50MB)
   * 3. Processing starts without Out of Memory errors
   * 4. Application remains responsive during processing
   *
   * ## Performance Note
   *
   * This test may be slow due to large file generation and processing.
   * Consider marking it as @slow or moving to a separate suite.
   */
  test('should handle ~10MB file without OOM crash', async ({ page }) => {
    await setupTestPage(page);

    /**
     * Generate a 10MB file filled with repeating pattern.
     * Pattern chosen to avoid triggering compression artifacts.
     */
    const fileSize = 10 * 1024 * 1024; // 10 MB
    const largeFileBytes = new Uint8Array(fileSize);

    /** Fill with pseudo-random-looking but deterministic pattern. */
    for (let i = 0; i < fileSize; i++) {
      largeFileBytes[i] = i % 256;
    }

    /** Track start time to detect excessive delays/freezes. */
    const uploadStart = Date.now();

    /** Upload the large file. */
    await page.evaluate((bytes) => {
      const file = new File([bytes], 'large-test.pdf', {
        type: 'application/pdf',
      });
      const dataTransfer = new DataTransfer();
      dataTransfer.items.add(file);

      const input = document.getElementById('drop-zone-input') as HTMLInputElement;
      if (input) {
        input.files = dataTransfer.files;
        input.dispatchEvent(new Event('change', { bubbles: true }));
      }
    }, Array.from(largeFileBytes));

    const uploadElapsed = Date.now() - uploadStart;

    console.log(`[Large File] Upload took ${uploadElapsed}ms`);

    /** Upload should complete within reasonable time (<30s for 10MB). */
    expect(uploadElapsed).toBeLessThan(30000);

    /** Wait for file info card to appear (indicates successful read). */
    const fileInfoCard = page.locator('#file-info-card');
    await expect(fileInfoCard).toHaveClass(/file-info-card--visible/, {
      timeout: UI_TRANSITION_TIMEOUT,
    });

    /**
     * Check if large file warning is displayed (>50MB threshold).
     * Our 10MB file shouldn't trigger it, but we verify the mechanism exists.
     */
    const largeFileWarning = page.locator('#file-info-large-file-warning');
    const warningVisible = await largeFileWarning.isVisible().catch(() => false);

    console.log(`[Large File] Warning shown (expect false for 10MB): ${warningVisible}`);

    /** 10MB < 50MB threshold, so warning should not be shown. */
    expect(warningVisible).toBe(false);

    /**
     * Optionally attempt sanitization (commented out by default due to time).
     * Uncomment to test actual processing of large files:
     *
     * await clickSanitize(page);
     * await waitForSanitizeComplete(page);
     * const result = await getSanitizeResult(page);
     * console.log(`[Large File] Sanitize result: ${JSON.stringify(result)}`);
     */
  });
});

