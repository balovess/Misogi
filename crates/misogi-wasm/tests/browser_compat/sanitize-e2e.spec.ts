test.describe('PDF Sanitization E2E', () => {

  /**
   * Setup: Navigate to page and ensure WASM is ready before each test.
   */
  test.beforeEach(async ({ page }) => {
    await setupTestPage(page);
  });

  /**
   * Test: Upload and sanitize a minimal valid PDF document.
   *
   * Verifies the complete happy path for PDF processing:
   * 1. File upload triggers type detection
   * 2. Sanitize button becomes available
   * 3. Clicking sanitize initiates WASM execution
   * 4. Processing completes within timeout
   * 5. Results panel displays threat information
   *
   * ## Note on Test Files
   *
   * This test uses generateMinimalPdf() to create a synthetic PDF inline.
   * For comprehensive testing with real content (embedded JS, forms, etc.),
   * use fixture files from tests/fixtures/ directory.
   */
  test('should sanitize a clean PDF without errors', async ({ page }) => {
    /** Generate a minimal PDF for testing (no external file needed). */
    const pdfBytes = generateMinimalPdf();

    /**
     * Create a temporary File object from the generated bytes.
     * This simulates what happens when a user selects a file via the UI.
     */
    await page.evaluate((bytes) => {
      const file = new File([bytes], 'test-minimal.pdf', { type: 'application/pdf' });
      const dataTransfer = new DataTransfer();
      dataTransfer.items.add(file);

      const input = document.getElementById('drop-zone-input') as HTMLInputElement;
      if (input) {
        input.files = dataTransfer.files;
        // Dispatch change event to trigger app.js handler
        input.dispatchEvent(new Event('change', { bubbles: true }));
      }
    }, Array.from(pdfBytes));

    /** Brief wait for file detection and UI update. */
    await page.waitForTimeout(1000);

    /** Verify file info card appeared (indicating successful upload). */
    const fileInfoCard = page.locator('#file-info-card');
    await expect(fileInfoCard).toHaveClass(/file-info-card--visible/, {
      timeout: UI_TRANSITION_TIMEOUT,
    });

    /** Initiate sanitization by clicking the button. */
    await clickSanitize(page);

    /** Wait for processing to complete (may take time for WASM execution). */
    await waitForSanitizeComplete(page);

    /** Extract and validate the sanitization result. */
    const result = await getSanitizeResult(page);

    /**
     * A minimal PDF should process successfully.
     * Threat count may be 0 (clean) or >0 depending on PDF structure.
     * The key assertion is success=true (no exceptions thrown).
     */
    expect(result.success).toBe(true);
    expect(result.threatsFound).toBeGreaterThanOrEqual(0);

    console.log(`[PDF Test] Result: success=${result.success}, threats=${result.threatsFound}`);
  });

  /**
   * Test: Verify error handling for non-PDF file when PDF path selected.
   *
   * If a user somehow uploads a non-PDF file (e.g., renamed .txt to .pdf),
   * the system should handle it gracefully rather than crashing.
   */
  test('should show error for invalid PDF structure', async ({ page }) => {
    /**
     * Create a fake PDF file (just renamed text).
     * This will fail PDF parsing but shouldn't crash the app.
     */
    const fakePdfContent = 'This is not a real PDF file';

    await page.evaluate((content) => {
      const file = new File([content], 'fake.pdf', { type: 'application/pdf' });
      const dataTransfer = new DataTransfer();
      dataTransfer.items.add(file);

      const input = document.getElementById('drop-zone-input') as HTMLInputElement;
      if (input) {
        input.files = dataTransfer.files;
        input.dispatchEvent(new Event('change', { bubbles: true }));
      }
    }, fakePdfContent);

    await page.waitForTimeout(1000);

    /** Attempt to sanitize the invalid file. */
    await clickSanitize(page);
    await waitForSanitizeComplete(page);

    /**
     * Either:
     * - Result shows success=false with error message, OR
     * - Error banner is displayed with Japanese text
     * Both are acceptable error handling outcomes.
     */
    const errorMessage = await getErrorMessage(page);
    const hasJapanese = await hasJapaneseErrorMessage(page);

    /** At minimum, we expect some form of error feedback to the user. */
    const hasErrorFeedback =
      errorMessage.length > 0 ||
      hasJapanese;

    expect(hasErrorFeedback).toBe(true);

    console.log(`[Invalid PDF Test] Error shown: ${errorMessage}`);
  });
});

