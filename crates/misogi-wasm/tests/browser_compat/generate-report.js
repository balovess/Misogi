/**
 * =============================================================================
 * Misogi WASM — Browser Compatibility Matrix Report Generator
 * =============================================================================
 *
 * Post-test script that parses Playwright JSON results and generates a
 * human-readable Markdown compatibility matrix. This report shows which
 * browsers passed/failed each test, making it easy to identify
 * browser-specific compatibility issues.
 *
 * ## Usage
 *
 * ```bash
 * # Run tests first (generates results.json)
 * npm test
 *
 * # Generate compatibility matrix report
 * node generate-report.js
 *
 * # Output: COMPATIBILITY_MATRIX.md in current directory
 * ```
 *
 * ## Report Contents
 *
 * 1. **Summary Table**: Pass/fail counts per browser project
 * 2. **Detailed Matrix**: Each test × browser cell with status icon
 * 3. **Failure Details**: Error messages and stack traces for failures
 * 4. **Environment Info**: Browser versions, OS, timestamps
 * 5. **Recommendations**: Action items based on test outcomes
 *
 * ## Output Format
 *
 * Generates a GitHub-flavored Markdown file suitable for:
 * - Direct viewing in GitHub/GitLab repositories
 * - Inclusion in CI/CD pipeline artifacts
 * - Attachment to pull request comments
 * - Documentation of supported browser versions
 *
 * @module generate-report
 * @requires ./results.json (generated by Playwright test run)
 * =============================================================================
 */

const fs = require('fs');
const path = require('path');

// =============================================================================
// Configuration
// =============================================================================

/** Input file path (Playwright JSON report output). */
const RESULTS_FILE = path.join(__dirname, 'results.json');

/** Output file path (generated compatibility matrix). */
const OUTPUT_FILE = path.join(__dirname, 'COMPATIBILITY_MATRIX.md');

/** Status icons for visual matrix display (GitHub-flavored Markdown compatible). */
const ICONS = {
  passed: '✅',
  failed: '❌',
  skipped: '⏭️',
  timedOut: '⏱️',
  unknown: '❓',
};

/** ANSI color codes for terminal output (optional enhancement). */
const COLORS = {
  reset: '\x1b[0m',
  green: '\x1b[32m',
  red: '\x1b[31m',
  yellow: '\x1b[33m',
  cyan: '\x1b[36m',
};

// =============================================================================
// Main Function
// =============================================================================

/**
 * Main entry point for report generation.
 *
 * Reads Playwright JSON results, processes them into a compatibility matrix,
 * and writes the formatted Markdown report to disk.
 */
function main() {
  console.log('\n📊 Misogi WASM Browser Compatibility Report Generator');
  console.log('=' .repeat(50));

  /** Check if results file exists. */
  if (!fs.existsSync(RESULTS_FILE)) {
    console.error(
      `${COLORS.red}Error:${COLORS_RESET} Results file not found: ${RESULTS_FILE}\n` +
      `Run tests first: ${COLORS.cyan}npm test${COLORS_RESET}`
    );
    process.exit(1);
  }

  /** Read and parse the Playwright JSON report. */
  console.log(`\nReading results from: ${RESULTS_FILE}`);
  const rawData = fs.readFileSync(RESULTS_FILE, 'utf-8');
  const results = JSON.parse(rawData);

  /** Process results into structured data for report generation. */
  const reportData = processResults(results);

  /** Generate the Markdown compatibility matrix. */
  const markdown = generateMarkdownReport(reportData);

  /** Write the report to disk. */
  fs.writeFileSync(OUTPUT_FILE, markdown, 'utf-8');

  /** Print summary to console. */
  printConsoleSummary(reportData);

  console.log(`\n${COLORS.green}✓ Report generated: ${OUTPUT_FILE}${COLORS.reset}\n`);
}

// =============================================================================
// Data Processing
// =============================================================================

/**
 * Process raw Playwright results into structured compatibility data.
 *
 * Transforms the flat suite/test structure from Playwright's JSON format
 * into a matrix format (tests × browsers) suitable for tabular display.
 *
 * @param {Object} results - Raw Playwright JSON report object.
 * @returns {Object} Structured data with suites, browsers, and matrix.
 */
function processResults(results) {
  const suites = [];
  const browsers = new Set();
  const matrix = {}; // { suiteName: { testName: { browserName: status } } }

  /**
   * Recursively extract test results from suite structure.
   * Playwright nests suites within suites, so we traverse the tree.
   */
  function extractTests(suite, suitePath = []) {
    const currentPath = [...suitePath, suite.title];

    /** Process individual test cases. */
    if (suite.tests && Array.isArray(suite.tests)) {
      for (const test of suite.tests) {
        const testName = test.title;
        const browserName = test.projectName || 'unknown';

        /** Track unique browser names for column headers. */
        browsers.add(browserName);

        /** Initialize nested structure if needed. */
        const suiteKey = currentPath.join(' > ');
        if (!matrix[suiteKey]) {
          matrix[suiteKey] = {};
        }
        if (!matrix[suiteKey][testName]) {
          matrix[suiteKey][testName] = {};
        }

        /** Determine test status from result code. */
        let status;
        switch (test.status) {
          case 'expected':
          case 'passed':
            status = 'passed';
            break;
          case 'unexpected':
          case 'failed':
            status = 'failed';
            break;
          case 'skipped':
          case 'pending':
            status = 'skipped';
            break;
          case 'timedOut':
            status = 'timedOut';
            break;
          default:
            status = 'unknown';
        }

        /** Store status and optional error details. */
        matrix[suiteKey][testName][browserName] = {
          status,
          duration: test.duration || 0,
          error: test.errors?.[0]?.message || null,
        };
      }
    }

    /** Recurse into child suites. */
    if (suite.suites && Array.isArray(suite.suites)) {
      for (const childSuite of suite.suites) {
        extractTests(childSuite, currentPath);
      }
    }

    /** Register this suite if it has tests. */
    if (suite.tests?.length > 0) {
      suites.push({
        name: currentPath.join(' > '),
        title: suite.title,
      });
    }
  }

  /** Start extraction from root suite. */
  if (results.suites) {
    for (const suite of results.suites) {
      extractTests(suite);
    }
  }

  return {
    suites,
    browsers: Array.from(browsers).sort(),
    matrix,
    metadata: {
      generatedAt: new Date().toISOString(),
      totalSuites: suites.length,
      totalBrowsers: browsers.size,
    },
  };
}

