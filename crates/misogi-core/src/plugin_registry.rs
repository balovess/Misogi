//! Global runtime plugin registry for Misogi's declarative macro SDK.
//!
//! This module provides a singleton [`PluginRegistry`] that serves as the central
//! discovery point for all plugins registered via the
//! [`#[misogi_plugin]`](misogi_macros::misogi_plugin) attribute macro.
//!
//! # Architecture
//!
//! ```text
//! ┌──────────────────────────────────────────────────────┐
//! │                   GLOBAL_REGISTRY                     │
//! │  Lazy<PluginRegistry> (once_cell, thread-safe init)  │
//! │  ┌────────────────────────────────────────────────┐  │
//! │  │ inner: RwLock<HashMap<String, PluginEntry>>    │  │
//! │  │                                                │  │
//! │  │ "korea_fss_compliance" → PluginEntry { ... }   │  │
//! │  │ "nist_zta_enforcer"     → PluginEntry { ... }   │  │
//! │  │ "acsc_au_scanner"       → PluginEntry { ... }   │  │
//! │  └────────────────────────────────────────────────┘  │
//! └──────────────────────────────────────────────────────┘
//!          ↑ register()           ↓ list_plugins()
//!     #[ctor] auto-reg         get_plugin()
//! ```
//!
//! # Thread Safety
//!
//! The registry uses `parking_lot::RwLock` for:
//! - **Multiple concurrent readers**: `list_plugins()`, `get_plugin()`, `plugin_count()`
//! - **Exclusive writer**: `register()` (called once per plugin at startup)
//!
//! The [`GLOBAL_REGISTRY`] is initialized lazily via `once_cell::sync::Lazy`,
//! guaranteeing it exists before any `#[ctor]` registration function runs.

use std::collections::HashMap;
use std::sync::Arc;

use parking_lot::RwLock;
use once_cell::sync::Lazy;

use crate::traits::PluginMetadata;

/// Metadata stored for each registered plugin in the registry.
#[derive(Debug, Clone)]
pub struct PluginEntry {
    /// Unique plugin identifier (matches [`PluginMetadata::name()`]).
    pub name: String,

    /// SemVer version string.
    pub version: String,

    /// Optional human-readable description.
    pub description: Option<String>,

    /// List of implemented interface names (from [`PluginMetadata::implemented_interfaces()`]).
    pub interfaces: Vec<String>,
}

/// Global, thread-safe plugin registry instance.
///
/// Initialized lazily on first access. All plugins registered via
/// `#[misogi_plugin]` are automatically inserted into this registry
/// at program startup (before `main()` runs).
pub static GLOBAL_REGISTRY: Lazy<PluginRegistry> = Lazy::new(|| {
    tracing::info!("Initializing Misogi Global Plugin Registry");
    PluginRegistry {
        inner: RwLock::new(HashMap::new()),
    }
});

/// Runtime registry for discovering and querying registered Misogi plugins.
///
/// Plugins are registered at startup via `#[ctor]` functions generated by
/// `#[misogi_plugin]`. After registration, they can be queried by name,
/// listed in bulk, or counted for monitoring purposes.
pub struct PluginRegistry {
    /// Internal map from plugin name to entry metadata.
    ///
    /// Protected by `RwLock` for concurrent read access with exclusive write.
    inner: RwLock<HashMap<String, PluginEntry>>,
}

impl PluginRegistry {
    /// Create a new empty registry (typically use [`GLOBAL_REGISTRY`] instead).
    pub fn new() -> Self {
        Self {
            inner: RwLock::new(HashMap::new()),
        }
    }

    /// Register a plugin into this registry.
    ///
    /// Called automatically by the `#[ctor]` function generated by
    /// `#[misogi_plugin]`. Manual registration is also supported for
    /// dynamic loading scenarios.
    ///
    /// # Arguments
    /// * `plugin` - Arc-wrapped plugin implementing [`PluginMetadata`].
    ///
    /// # Errors
    ///
    /// Returns [`MisogiError::Configuration`] if a plugin with the same name
    /// is already registered (duplicate detection).
    ///
    /// # Example
    ///
    /// ```rust,ignore
    /// let my_plugin = Arc::new(MyCompliancePlugin);
    /// misogi_core::plugin_registry::GLOBAL_REGISTRY.register(my_plugin)?;
    /// ```
    pub fn register(&self, plugin: Arc<dyn PluginMetadata>) -> crate::error::Result<()> {
        let name = plugin.name().to_string();
        let version = plugin.version().to_string();
        let description = plugin.description().map(|s| s.to_string());
        let interfaces = plugin
            .implemented_interfaces()
            .into_iter()
            .map(|s| s.to_string())
            .collect();

        let mut map = self.inner.write();
        if map.contains_key(&name) {
            return Err(crate::error::MisogiError::Configuration(format!(
                "Plugin '{}' is already registered (version {})",
                name,
                map.get(&name).map(|e| e.version.as_str()).unwrap_or("?")
            )));
        }

        let entry = PluginEntry {
            name: name.clone(),
            version: version.clone(),
            description,
            interfaces,
        };

        map.insert(name.clone(), entry);

        tracing::debug!(
            plugin_name = %name,
            plugin_version = %version,
            total_plugins = map.len(),
            "Plugin registered into GLOBAL_REGISTRY"
        );

        Ok(())
    }

    /// List all currently registered plugins.
    ///
    /// Returns a snapshot of all plugin entries at the time of the call.
    /// The returned vector is ordered by registration order (insertion order).
    ///
    /// # Example
    ///
    /// ```rust,ignore
    /// for plugin in misogi_core::plugin_registry::GLOBAL_REGISTRY.list_plugins() {
    ///     println!("{} v{} — {}", plugin.name, plugin.version,
    ///              plugin.description.as_deref().unwrap_or("(no description)"));
    /// }
    /// ```
    pub fn list_plugins(&self) -> Vec<PluginEntry> {
        self.inner.read().values().cloned().collect()
    }

    /// Look up a specific plugin by its unique name.
    ///
    /// # Arguments
    /// * `name` - The exact plugin identifier (kebab-case).
    ///
    /// # Returns
    ///
    /// - `Some(PluginEntry)` if the plugin is registered.
    /// - `None` if no plugin with that name exists.
    pub fn get_plugin(&self, name: &str) -> Option<PluginEntry> {
        self.inner.read().get(name).cloned()
    }

    /// Return the total number of registered plugins.
    ///
    /// Useful for health checks and monitoring dashboards to verify
    /// that expected plugins have loaded successfully.
    pub fn plugin_count(&self) -> usize {
        self.inner.read().len()
    }

    /// Check whether a specific plugin name is registered.
    ///
    /// Faster than `get_plugin()` when only existence checking is needed
    /// (avoids cloning the full entry).
    pub fn has_plugin(&self, name: &str) -> bool {
        self.inner.read().contains_key(name)
    }

    /// Remove a plugin from the registry (for hot-reload / testing).
    ///
    /// # Arguments
    /// * `name` - The plugin identifier to remove.
    ///
    /// # Returns
    ///
    /// - `Some(PluginEntry)` if the plugin was removed.
    /// - `None` if the plugin was not found.
    #[allow(dead_code)]
    pub fn unregister(&self, name: &str) -> Option<PluginEntry> {
        let mut map = self.inner.write();
        map.remove(name)
    }
}

impl Default for PluginRegistry {
    fn default() -> Self {
        Self::new()
    }
}

