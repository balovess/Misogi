#[cfg(test)]
mod tests {
    use super::*;
    use std::sync::Arc;

    struct MockPlugin;

    impl PluginMetadata for MockPlugin {
        fn name(&self) -> &'static str { "test_mock_plugin" }
        fn version(&self) -> &'static str { "0.1.0" }
        fn description(&self) -> Option<&'static str> { Some("A test mock plugin") }
        fn implemented_interfaces(&self) -> Vec<&'static str> {
            vec!["PluginMetadata", "CDRStrategy"]
        }
    }

    #[test]
    fn test_register_and_retrieve() {
        let registry = PluginRegistry::new();
        let plugin = Arc::new(MockPlugin);

        assert!(registry.register(plugin).is_ok());
        assert_eq!(registry.plugin_count(), 1);

        let entry = registry.get_plugin("test_mock_plugin");
        assert!(entry.is_some());
        let e = entry.unwrap();
        assert_eq!(e.name, "test_mock_plugin");
        assert_eq!(e.version, "0.1.0");
        assert_eq!(e.description.as_deref(), Some("A test mock plugin"));
        assert_eq!(e.interfaces.len(), 2);
    }

    #[test]
    fn test_duplicate_registration_fails() {
        let registry = PluginRegistry::new();
        let p1 = Arc::new(MockPlugin);
        let p2 = Arc::new(MockPlugin);

        assert!(registry.register(p1).is_ok());
        assert!(registry.register(p2).is_err());
        assert_eq!(registry.plugin_count(), 1);
    }

    #[test]
    fn test_list_plugins_returns_all() {
        let registry = PluginRegistry::new();
        assert!(registry.list_plugins().is_empty());

        let _ = registry.register(Arc::new(MockPlugin));
        assert_eq!(registry.list_plugins().len(), 1);
    }

    #[test]
    fn test_has_plugin() {
        let registry = PluginRegistry::new();
        assert!(!registry.has_plugin("nonexistent"));

        let _ = registry.register(Arc::new(MockPlugin));
        assert!(registry.has_plugin("test_mock_plugin"));
    }

    #[test]
    fn test_unregister_removes_plugin() {
        let registry = PluginRegistry::new();
        let _ = registry.register(Arc::new(MockPlugin));
        assert_eq!(registry.plugin_count(), 1);

        let removed = registry.unregister("test_mock_plugin");
        assert!(removed.is_some());
        assert_eq!(registry.plugin_count(), 0);
    }

    #[test]
    fn test_global_registry_exists() {
        // Global registry should be initialized and accessible
        // (plugin count may be 0 if no plugins are registered at test time)
        // Note: plugin_count() returns usize (unsigned), so >= 0 is always true
        let _count = GLOBAL_REGISTRY.plugin_count();
        assert!(_count <= usize::MAX);  // Trivially true, validates type system
    }
}
